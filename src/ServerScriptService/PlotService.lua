-- Non-stands buildings visibly GROW with their upgrade level so progression is
-- felt. Scales around the centre, then re-seats the base on the ground (y=0).
local function scaleBuildingToLevel(model, level)
	local base = model.PrimaryPart
	if not base then return end
	local targetScale = 1 + math.clamp(level, 0, 24) * 0.03   -- up to ~1.7x at high levels
	model:ScaleTo(targetScale)
	local baseBottom = base.Position.Y - base.Size.Y / 2
	if math.abs(baseBottom) > 0.001 then
		model:TranslateBy(Vector3.new(0, -baseBottom, 0))
	end
end

-- ─── Plot builder ────────────────────────────────────────────────────────────

function PlotService.buildPlot(player)
	-- Avoid duplicate plots if called twice
	if playerPlotModel[player.UserId] then
		return playerPlotModel[player.UserId]
	end

	local slot   = allocateSlot(player)
	local origin = PLOT_SLOTS[slot]

	local plot = Instance.new("Model")
	plot.Name = "Plot_" .. player.UserId

	-- Solid, collidable ground pad — the single guaranteed walking surface for the
	-- whole base. Top sits exactly at y = 0; everything else is placed on top of it.
	local pad = Instance.new("Part")
	pad.Name         = "Ground"
	pad.Anchored     = true
	pad.CanCollide   = true
	pad.Size         = Vector3.new(PLOT_SIZE.X, 4, PLOT_SIZE.Z)
	pad.Position     = origin + Vector3.new(0, -2, 0)   -- top at y = 0
	pad.Color        = Color3.fromRGB(70, 112, 72)
	pad.Material     = Enum.Material.Grass
	pad.TopSurface   = Enum.SurfaceType.Smooth
	pad.BottomSurface = Enum.SurfaceType.Smooth
	pad.Parent       = plot
	plot.PrimaryPart = pad
	applyTexture(pad, TEXTURES.grass, 24, { Enum.NormalId.Top })   -- mowed-grass surface

	-- Owner reference + spawn point
	local ownerValue = Instance.new("ObjectValue")
	ownerValue.Name   = "Owner"
	ownerValue.Value  = player
	ownerValue.Parent = plot

	-- Entrance plaza spawn at the south edge, facing north into the grounds.
	local spawnPos = origin + Vector3.new(0, 1, -PLOT_SIZE.Z / 2 + 11)
	local spawnPad = Instance.new("Part")
	spawnPad.Name        = "PlotSpawn"
	spawnPad.Size        = Vector3.new(14, 1, 14)
	spawnPad.Position    = spawnPos
	spawnPad.Anchored    = true
	spawnPad.Color       = Color3.fromRGB(60, 64, 76)
	spawnPad.Material    = Enum.Material.Concrete
	spawnPad.TopSurface  = Enum.SurfaceType.Smooth
	spawnPad.Parent      = plot

	-- Floating base-name sign (visible over the walls, identifies whose base it is).
	local nameSign = Instance.new("BillboardGui")
	nameSign.Name        = "BaseName"
	nameSign.Size        = UDim2.new(0, 280, 0, 54)
	nameSign.StudsOffset = Vector3.new(0, 26, 0)
	nameSign.AlwaysOnTop = true
	nameSign.MaxDistance = 400
	nameSign.Adornee     = spawnPad
	nameSign.Parent      = spawnPad
	local nameLbl = Instance.new("TextLabel")
	nameLbl.Size = UDim2.new(1, 0, 1, 0)
	nameLbl.BackgroundTransparency = 1
	nameLbl.Text = player.DisplayName .. "'s Stadium"
	nameLbl.TextColor3 = Color3.fromRGB(255, 235, 150)
	nameLbl.TextScaled = true
	nameLbl.Font = Enum.Font.GothamBlack
	nameLbl.TextStrokeTransparency = 0.4
	nameLbl.Parent = nameSign

	-- Stored facing +Z so the player looks into their stadium on (re)spawn.
	playerSpawnCFrame[player.UserId] =
		CFrame.lookAt(spawnPos + Vector3.new(0, 3, 0), spawnPos + Vector3.new(0, 3, 10))

	-- Pitch in the centre, advertising hoardings, perimeter wall + goals, gate
	buildPitch(plot, origin)
	buildHoardings(plot, origin)
	buildSurrounds(plot, origin)
	buildDoor(plot, origin, player)
	buildKartStation(plot, origin, player)
	buildCityPad(plot, origin, player)
	buildRebirthPad(plot, origin, player)
	buildUpperTier(plot, origin, player)

	-- Place each building at its hand-picked spot (spacious, framing the plaza),
	-- built to its current upgrade level (stands grow their seating with level).
	local data = DataService.getData(player)
	for _, buildingCfg in ipairs(BuildingConfig.Buildings) do
		local spot = BUILDING_LAYOUT[buildingCfg.id] or { x = 0, z = 0 }
		local pos = Vector3.new(
			origin.X + spot.x,
			origin.Y,                 -- ground pad top is y = 0
			origin.Z + spot.z
		)
		local level = (data and data.stadium and data.stadium[buildingCfg.id]) or 0
		local m = buildBuildingModel(buildingCfg, pos, player, level)
		m.Parent = plot
		-- Turn the concourse shops so their storefronts face the pitch.
		if buildingCfg.id == "concessions" or buildingCfg.id == "merch" then
			local target = Vector3.new(origin.X, m:GetPivot().Position.Y, origin.Z + 6)
			m:PivotTo(CFrame.lookAt(m:GetPivot().Position, target))
		end
		if buildingCfg.id ~= "stands" and level > 0 then
			scaleBuildingToLevel(m, level)
		end
	end

	plot.Parent = plotsFolder
	playerPlotModel[player.UserId] = plot

	-- Teleport onto the plot now AND on every respawn (fixes spawning at world origin).
	playerConns[player.UserId] = player.CharacterAdded:Connect(function()
		teleportToPlot(player)
	end)
	task.spawn(teleportToPlot, player)

	return plot
end

-- Called after a successful upgrade so the stadium visibly builds up in real time.
function PlotService.onBuildingUpgraded(player, buildingId, level)
	local plot  = playerPlotModel[player.UserId]
	local index = playerPlotIndex[player.UserId]
	if not plot or not index then return end
	local model = plot:FindFirstChild(buildingId)
	if not model then return end

	if buildingId == "stands" then
		-- The grandstand rebuilds its seating/crowd/roof to the new level.
		local origin = PLOT_SLOTS[index]
		local spot   = BUILDING_LAYOUT.stands
		buildStandTiers(model, origin.X + spot.x, origin.Z + spot.z, level)
	else
		-- Everything else grows in scale.
		scaleBuildingToLevel(model, level)
	end
end

-- Where the player (re)spawns on their plot — used by HubService to send them home.
function PlotService.getSpawnCFrame(player)
	return playerSpawnCFrame[player.UserId]
end

function PlotService.removePlot(player)
	local conn = playerConns[player.UserId]
	if conn then
		conn:Disconnect()
		playerConns[player.UserId] = nil
	end
	playerSpawnCFrame[player.UserId] = nil

	local plot = playerPlotModel[player.UserId]
	if plot then
		plot:Destroy()
		playerPlotModel[player.UserId] = nil
	end
	-- Free the slot and put the "OPEN PLOT" placeholder back so the spot is reusable.
	local slot = freeSlot(player)
	if slot then buildEmptyPlot(slot) end
end

-- Build "OPEN PLOT" placeholders on every slot not currently claimed. Called once
-- at server start (after the city exists) so the world looks populated from join 1.
function PlotService.initWorld()
	for i = 1, #PLOT_SLOTS do
		if not usedSlots[i] then
			buildEmptyPlot(i)
		end
	end
end

return PlotService
