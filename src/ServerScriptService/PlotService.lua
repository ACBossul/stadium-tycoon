-- Raked seating rows for the stands that GROW with the upgrade level. Each row
-- steps up and back; more rows = a taller, fuller grandstand. Re-runnable: it
-- clears its old rows first, so it doubles as the "regrow on upgrade" path.
-- (baseX, baseZ) is the stands' world position; rows sit on its y=3 foundation.
local SPECTATOR_SHIRTS = {
	Color3.fromRGB(222, 72, 72),  Color3.fromRGB(70, 120, 222), Color3.fromRGB(245, 205, 55),
	Color3.fromRGB(60, 182, 104), Color3.fromRGB(232, 232, 238), Color3.fromRGB(160, 92, 210),
	Color3.fromRGB(250, 140, 40),
}

local function buildStandTiers(model, baseX, baseZ, level)
	-- Clear previously-built dynamic stand parts (re-runnable for live regrow).
	for _, c in ipairs(model:GetChildren()) do
		if c.Name == "StandPart" then c:Destroy() end
	end

	local rows     = math.clamp(level, 1, 8)   -- seating rows grow with level
	local stepUp   = 2.2
	local stepBack = 2.6
	local W        = 40                          -- stand width
	local frontZ   = baseZ - 6                   -- front row (nearest the pitch)
	local backZ    = frontZ + (rows - 1) * stepBack
	local midZ     = (frontZ + backZ) / 2
	local depth    = (backZ - frontZ) + 8
	local seatTopY = 3 + rows * stepUp           -- y at the top of the seating bank
	local roofY    = seatTopY + 8                -- canopy floats high overhead (real clearance)

	local STEP_C = Color3.fromRGB(74, 78, 92)
	local WALL_C = Color3.fromRGB(88, 92, 106)
	local ROOF_C = Color3.fromRGB(38, 42, 52)
	local GOLD   = Color3.fromRGB(245, 205, 55)
	local GREEN  = Color3.fromRGB(48, 170, 90)
	local SKIN   = Color3.fromRGB(236, 200, 168)

	local function part(sx, sy, sz, x, y, z, color, mat, collide, shape)
		local p = Instance.new("Part")
		p.Name          = "StandPart"
		p.Anchored      = true
		p.CanCollide    = collide ~= false
		p.Size          = Vector3.new(sx, sy, sz)
		p.Position      = Vector3.new(x, y, z)
		p.Color         = color
		p.Material      = mat or Enum.Material.SmoothPlastic
